# Basic if statement examples

# Example 1: Simple age check
age = 18
if age >= 18:
    print("You are an adult!")

# Example 2: Positive number check
number = 10
if number > 0:
    print(f"{number} is positive")

# Example 3: String comparison
username = "admin"
if username == "admin":
    print("Admin access granted")

# Example 4: List membership check
fruits = ["apple", "banana", "orange"]
if "banana" in fruits:
    print("Banana is in the list")

# Example 5: Multiple conditions
temperature = 25
weather = "sunny"
if temperature > 20 and weather == "sunny":
    print("Good day for outdoor activities")